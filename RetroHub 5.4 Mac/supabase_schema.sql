-- PROFILES
CREATE POLICY "Anyone can read profiles"
    ON public.profiles FOR SELECT USING (true);

CREATE POLICY "Users can insert their own profile"
    ON public.profiles FOR INSERT
    WITH CHECK (auth.uid() = id OR auth.role() = 'service_role');

CREATE POLICY "Users can update their own profile"
    ON public.profiles FOR UPDATE USING (auth.uid() = id);

CREATE POLICY "Admins can update any profile"
    ON public.profiles FOR UPDATE USING (
        EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true)
    );

CREATE POLICY "Admins can delete profiles"
    ON public.profiles FOR DELETE USING (
        EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true)
    );

-- CATEGORIES
CREATE POLICY "Anyone can read categories"
    ON public.categories FOR SELECT USING (true);

CREATE POLICY "Only admins can modify categories"
    ON public.categories FOR ALL USING (
        EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true)
    );

-- PRODUCTS
CREATE POLICY "Anyone can read all products"
    ON public.products FOR SELECT USING (true);

CREATE POLICY "Sellers can insert their own products"
    ON public.products FOR INSERT WITH CHECK (
        auth.uid() = seller_id AND EXISTS (
            SELECT 1 FROM public.profiles
            WHERE id = auth.uid() AND is_seller = true AND seller_status = 'active'
        )
    );

CREATE POLICY "Sellers can update their own products"
    ON public.products FOR UPDATE USING (auth.uid() = seller_id);

CREATE POLICY "Sellers can delete their own products"
    ON public.products FOR DELETE USING (auth.uid() = seller_id);

CREATE POLICY "Admins can manage all products"
    ON public.products FOR ALL USING (
        EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true)
    );

-- BIDS
CREATE POLICY "Anyone can read bids"
    ON public.bids FOR SELECT USING (true);

CREATE POLICY "Authenticated buyers can bid"
    ON public.bids FOR INSERT WITH CHECK (
        auth.uid() = bidder_id AND NOT EXISTS (
            SELECT 1 FROM public.profiles
            WHERE id = auth.uid() AND suspend_until > now()
        )
    );

-- ORDERS
CREATE POLICY "Authenticated buyers can create orders"
    ON public.orders FOR INSERT WITH CHECK (auth.uid() = buyer_id);

CREATE POLICY "Involved parties or admin can view order"
    ON public.orders FOR SELECT USING (
        auth.uid() = buyer_id OR auth.uid() = seller_id OR
        EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true)
    );

CREATE POLICY "Involved parties or admin can update order"
    ON public.orders FOR UPDATE USING (
        auth.uid() = buyer_id OR auth.uid() = seller_id OR
        EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true)
    );

-- CHAT MESSAGES
CREATE POLICY "Users can view chats they are part of"
    ON public.chat_messages FOR SELECT USING (
        auth.uid() = sender_id OR
        room_id LIKE '%' || auth.uid()::text || '%' OR
        EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true)
    );

CREATE POLICY "Users can send chats they are part of"
    ON public.chat_messages FOR INSERT WITH CHECK (auth.uid() = sender_id);

CREATE POLICY "Receivers can mark their messages as read"
    ON public.chat_messages FOR UPDATE USING (
        auth.uid() = receiver_id
    ) WITH CHECK (
        auth.uid() = receiver_id
    );

-- BUYER ADDRESSES
CREATE POLICY "Users manage own addresses"
    ON public.buyer_addresses FOR ALL
    USING (auth.uid() = user_id)
    WITH CHECK (auth.uid() = user_id);

-- DISPUTES
CREATE POLICY "Involved parties can view dispute"
    ON public.disputes FOR SELECT USING (
        auth.uid() = buyer_id OR auth.uid() = seller_id OR
        EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true)
    );

CREATE POLICY "Buyer can create dispute"
    ON public.disputes FOR INSERT WITH CHECK (auth.uid() = buyer_id);

CREATE POLICY "Admin can update dispute"
    ON public.disputes FOR UPDATE USING (
        EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true)
    );


