-- 8. TRIGGER: Cegah Manipulasi Harga & Judul Produk
-- ============================================================
CREATE OR REPLACE FUNCTION public.prevent_product_detail_updates()
RETURNS TRIGGER AS $$
DECLARE
    has_orders BOOLEAN;
BEGIN
    IF (OLD.title IS DISTINCT FROM NEW.title OR
        OLD.transaction_type IS DISTINCT FROM NEW.transaction_type)
    THEN
        IF NOT EXISTS (
            SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true
        ) THEN
            RAISE EXCEPTION 'Mengubah judul produk atau jenis transaksi tidak diperbolehkan.';
        END IF;
    END IF;

    IF (OLD.price IS DISTINCT FROM NEW.price OR
        OLD.starting_bid IS DISTINCT FROM NEW.starting_bid OR
        OLD.bid_increment IS DISTINCT FROM NEW.bid_increment OR
        OLD.buyout_price  IS DISTINCT FROM NEW.buyout_price)
    THEN
        IF NOT EXISTS (
            SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true
        ) THEN
            SELECT EXISTS(SELECT 1 FROM public.orders WHERE product_id = OLD.id) INTO has_orders;

            IF has_orders THEN
                IF NEW.price IS NOT NULL AND OLD.price IS NOT NULL AND
                   (NEW.price > OLD.price * 1.5 OR NEW.price < OLD.price * 0.5) THEN
                    RAISE EXCEPTION 'Perubahan harga maksimal +/-50%% karena produk ini sudah memiliki histori transaksi.';
                END IF;
                IF NEW.starting_bid IS NOT NULL AND OLD.starting_bid IS NOT NULL AND
                   (NEW.starting_bid > OLD.starting_bid * 1.5 OR NEW.starting_bid < OLD.starting_bid * 0.5) THEN
                    RAISE EXCEPTION 'Perubahan harga awal lelang maksimal +/-50%% karena produk ini sudah memiliki histori transaksi.';
                END IF;
                IF NEW.buyout_price IS NOT NULL AND OLD.buyout_price IS NOT NULL AND
                   (NEW.buyout_price > OLD.buyout_price * 1.5 OR NEW.buyout_price < OLD.buyout_price * 0.5) THEN
                    RAISE EXCEPTION 'Perubahan harga buyout maksimal +/-50%% karena produk ini sudah memiliki histori transaksi.';
                END IF;
            END IF;
        END IF;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

CREATE TRIGGER trigger_prevent_product_detail_updates
BEFORE UPDATE ON public.products
FOR EACH ROW EXECUTE FUNCTION public.prevent_product_detail_updates();


