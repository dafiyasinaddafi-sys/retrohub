-- 6. TRIGGER: Sensor Kata Terlarang pada Produk
-- ============================================================
CREATE OR REPLACE FUNCTION public.check_prohibited_words()
RETURNS TRIGGER AS $$
DECLARE
    prohibited_pattern TEXT;
BEGIN
    prohibited_pattern := '\y(hewan|binatang|kucing|anjing|ular|burung|makanan|minuman|cemilan|obat|suplemen|organ|darah|senjata|pistol|pisau|airsoft|judi|slot|chip|bokep|dewasa|porn|narkoba|sabu|ganja|rokok|vape)\y';

    IF (NEW.title ~* prohibited_pattern) OR (NEW.description ~* prohibited_pattern) THEN
        NEW.flagged := true;
    ELSE
        NEW.flagged := false;
    END IF;

    RETURN NEW; -- WAJIB: tanpa ini insert/update dibatalkan diam-diam
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

CREATE TRIGGER before_product_insert_update
BEFORE INSERT OR UPDATE ON public.products
FOR EACH ROW EXECUTE FUNCTION public.check_prohibited_words();


