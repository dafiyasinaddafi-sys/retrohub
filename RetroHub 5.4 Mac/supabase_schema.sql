CREATE TYPE product_condition AS ENUM ('MIB', 'BIB', 'Loose');
CREATE TYPE transaction_type  AS ENUM ('sale', 'lelang');
CREATE TYPE order_status      AS ENUM (
    'waiting_payment', 'to_ship', 'shipping',
    'delivered', 'completed', 'disputed'
);


-- ============================================================
-- 2. TABEL UTAMA
-- ============================================================

-- A. Profiles (terhubung ke Supabase Auth)
CREATE TABLE public.profiles (
    id                   UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    email                TEXT NOT NULL,
    full_name            TEXT NOT NULL,
    store_name           TEXT UNIQUE,

    -- Role: 3 kolom boolean (1 user bisa buyer + seller sekaligus)
    is_buyer             BOOLEAN NOT NULL DEFAULT true,
    is_seller            BOOLEAN NOT NULL DEFAULT false,
    is_admin             BOOLEAN NOT NULL DEFAULT false,
    seller_status        TEXT    DEFAULT NULL, -- NULL | 'pending' | 'active' | 'rejected'

    -- Kontak & verifikasi seller
    whatsapp             TEXT,
    ktp_photo_url        TEXT,
    ktp_selfie_url       TEXT,
    other_store_link     TEXT,

    -- Alamat lengkap
    address_provinsi     TEXT,
    address_provinsi_id  TEXT,
    address_kota         TEXT,
    address_kota_id      TEXT,
    address_kecamatan    TEXT,
    address_kecamatan_id TEXT,
    address_kelurahan    TEXT,
    address_kelurahan_id TEXT,
    address_kodepos      TEXT,
    address_lat          NUMERIC(10,7),
    address_lng          NUMERIC(10,7),
    address_detail       TEXT,
    cart_items           JSONB DEFAULT '[]',

    -- Keuangan
    wallet_balance       NUMERIC(12,2) DEFAULT 0,
    rating               NUMERIC(3,2)  DEFAULT 0,
    reviews_count        INTEGER       DEFAULT 0,

    -- Sistem penalti & suspend
    warning_count        INTEGER   DEFAULT 0,
    suspend_until        TIMESTAMP WITH TIME ZONE,
    penalty_points       INTEGER   NOT NULL DEFAULT 0,
    last_penalty_decay   TIMESTAMP WITH TIME ZONE,

    created_at           TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc', now()) NOT NULL
);

CREATE INDEX idx_profiles_kota     ON public.profiles (address_kota);
CREATE INDEX idx_profiles_kodepos  ON public.profiles (address_kodepos);


-- B. Categories (dikelola admin)
CREATE TABLE public.categories (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name           TEXT NOT NULL UNIQUE,
    icon           TEXT,
    sub_categories JSONB NOT NULL DEFAULT '["Umum"]'::jsonb
);


-- C. Products
CREATE TABLE public.products (
    id               UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    seller_id        UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    title            TEXT NOT NULL,
    description      TEXT,
    category_id      BIGINT NOT NULL REFERENCES public.categories(id) ON DELETE RESTRICT,
    sub_category     TEXT NOT NULL DEFAULT 'Umum',
    condition        product_condition NOT NULL,
    transaction_type transaction_type  NOT NULL,
    weight_grams     INTEGER NOT NULL DEFAULT 100,
    stock            INTEGER NOT NULL DEFAULT 1,
    price            NUMERIC(12,2),
    starting_bid     NUMERIC(12,2),
    bid_increment    NUMERIC(12,2),
    buyout_price     NUMERIC(12,2),
    duration_hours   INTEGER DEFAULT 24,
    auction_end_time TIMESTAMP WITH TIME ZONE,
    flagged          BOOLEAN NOT NULL DEFAULT false,
    status           TEXT    NOT NULL DEFAULT 'active',
    created_at       TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc', now()) NOT NULL,

    CONSTRAINT check_pricing CHECK (
        (transaction_type = 'sale'   AND price IS NOT NULL) OR
        (transaction_type = 'lelang' AND starting_bid IS NOT NULL AND bid_increment IS NOT NULL)
    )
);


-- D. Bids (histori penawaran lelang)
CREATE TABLE public.bids (
    id         UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    product_id UUID NOT NULL REFERENCES public.products(id) ON DELETE CASCADE,
    bidder_id  UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    amount     NUMERIC(12,2) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc', now()) NOT NULL
);


-- E. Orders (escrow rekber)
CREATE TABLE public.orders (
    id                       UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    product_id               UUID NOT NULL REFERENCES public.products(id)  ON DELETE RESTRICT,
    buyer_id                 UUID NOT NULL REFERENCES public.profiles(id)   ON DELETE RESTRICT,
    seller_id                UUID NOT NULL REFERENCES public.profiles(id)   ON DELETE RESTRICT,

    -- Snapshot produk saat order dibuat
    product_title            TEXT,
    product_description      TEXT,
    product_image_url        TEXT,
    seller_name              TEXT,
    buyer_name               TEXT,

    -- Harga & pembayaran
    price_deal               NUMERIC(12,2) NOT NULL,
    quantity                 INTEGER       NOT NULL DEFAULT 1,
    shipping_cost            NUMERIC(12,2) NOT NULL,
    admin_fee                NUMERIC(12,2) NOT NULL,
    total_payment            NUMERIC(12,2) NOT NULL,
    payment_status           TEXT          NOT NULL DEFAULT 'pending',
    paid_at                  TIMESTAMP WITH TIME ZONE,

    -- Pengiriman
    shipping_courier         TEXT NOT NULL,
    shipping_address         TEXT,
    recipient_name           TEXT,
    recipient_phone          TEXT,
    address_label            TEXT,
    address_patokan          TEXT,
    tracking_number          TEXT,
    shipped_at               TIMESTAMP WITH TIME ZONE,

    -- Status & timeline
    status                   order_status NOT NULL DEFAULT 'waiting_payment',
    seller_accepted_at       TIMESTAMP WITH TIME ZONE,
    delivered_at             TIMESTAMP WITH TIME ZONE,
    completed_at             TIMESTAMP WITH TIME ZONE,
    refunded_at              TIMESTAMP WITH TIME ZONE,

    -- Bukti pengiriman
    buyer_unboxing_video_url TEXT,
    seller_packing_video_url TEXT,

    -- Keuangan seller
    withdrawn_status         TEXT DEFAULT 'available',

    -- Catatan buyer
    buyer_notes              TEXT,

    created_at               TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc', now()) NOT NULL
);

CREATE INDEX idx_orders_buyer_id   ON public.orders(buyer_id);
CREATE INDEX idx_orders_seller_id  ON public.orders(seller_id);
CREATE INDEX idx_orders_status     ON public.orders(status);
CREATE INDEX idx_orders_created_at ON public.orders(created_at DESC);


-- F. Chat Messages
CREATE TABLE public.chat_messages (
    id           UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    room_id      TEXT NOT NULL,
    sender_id    UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    receiver_id  UUID          REFERENCES public.profiles(id) ON DELETE CASCADE,
    message_text TEXT NOT NULL,
    read         BOOLEAN NOT NULL DEFAULT false,
    media_url    TEXT,
    media_type   TEXT,
    created_at   TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc', now()) NOT NULL
);


-- G. Buku Alamat Pengiriman Buyer
CREATE TABLE public.buyer_addresses (
    id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    user_id         UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    label           TEXT NOT NULL DEFAULT 'Rumah',
    recipient_name  TEXT NOT NULL,
    recipient_phone TEXT NOT NULL,
    address_detail  TEXT NOT NULL,
    kecamatan       TEXT,
    kota            TEXT,
    provinsi        TEXT,
    kode_pos        TEXT,
    patokan         TEXT,
    is_default      BOOLEAN DEFAULT false,
    created_at      TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc', now()) NOT NULL
);

CREATE INDEX idx_buyer_addresses_user_id ON public.buyer_addresses(user_id);


-- H. Disputes (sistem komplain)
CREATE TABLE public.disputes (
    id             TEXT PRIMARY KEY,
    order_id       TEXT NOT NULL,
    buyer_id       UUID NOT NULL REFERENCES public.profiles(id),
    seller_id      UUID NOT NULL REFERENCES public.profiles(id),
    reason         TEXT NOT NULL,
    detail         TEXT NOT NULL,
    photo_urls     JSONB   DEFAULT '[]',
    video_url      TEXT,
    video_filename TEXT,
    video_size_mb  NUMERIC,
    photo_count    INTEGER DEFAULT 0,
    status         TEXT NOT NULL DEFAULT 'pending',
    admin_note     TEXT DEFAULT '',
    created_at     TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc', now()) NOT NULL,
    resolved_at    TIMESTAMP WITH TIME ZONE
);

CREATE INDEX idx_disputes_order_id ON public.disputes(order_id);
CREATE INDEX idx_disputes_buyer_id ON public.disputes(buyer_id);
CREATE INDEX idx_disputes_status   ON public.disputes(status);


